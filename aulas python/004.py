# Faça um programa que leia a
# algo pelo teclado e mostre na tela
# seu tipo primitivo e todas as possiveis informaçoes sobre ela

n = input("digite algo: ")
print(type(n))
print(n.capitalize())
print(n.casefold())
print(n.isnumeric())
print(n.isalnum())
print(n.islower())
print(n.isalpha())
print(n.isdecimal())
print(n.isupper())
print(n.isprintable())
